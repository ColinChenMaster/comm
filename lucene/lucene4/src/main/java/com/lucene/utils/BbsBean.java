package com.lucene.utils;

/**
 * 传入的Bean
 * @author totyuZWL
 *
 */
public class BbsBean {

	private String topicId;
	
	private String title;
	
	private String revertContent;
	
	private String revertUrl;

	public String getTopicId() {
		return topicId;
	}

	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getRevertContent() {
		return revertContent;
	}

	public void setRevertContent(String revertContent) {
		this.revertContent = revertContent;
	}

	public String getRevertUrl() {
		return revertUrl;
	}

	public void setRevertUrl(String revertUrl) {
		this.revertUrl = revertUrl;
	}

}
